package initializers

import (
	"log"
	"os"

	"gorm.io/driver/postgres"
	"gorm.io/gorm"
)

var DB *gorm.DB

func ConnectToDB() {
	//THIS IS A PLACEHOLDER FOR NOW. WE NEED TO SET UP OUR DATABASE TO GET THE CREDENTIALS (ENV FILE)
	//Also: question Victor how to keep credentials hidden while repo is public
	var err error
	dsn := os.Getenv("DB_URL")
	DB, err = gorm.Open(postgres.Open(dsn), &gorm.Config{})

	if err != nil {
		log.Fatal("Failed to connect to database")
	}
}
